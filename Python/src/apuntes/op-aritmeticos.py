# Ejemplos de operaciones aritméticas

# Suma
resultado = 5 + 4
print("5+4 = ",resultado)


# Resta
resultado = 5 - 4
print("5-4 = ",resultado)


# Multiplicación
resultado = 5 * 4
print("5*4 = ",resultado)


# División
resultado = 5 / 4
print("5/4 = ",resultado)


# Módulo (resto de la división entera)
resultado = 5 % 4
print("5%4 = ",resultado)


# Exponenciación
resultado = 5 ^ 4
print("5^4 = ",resultado)