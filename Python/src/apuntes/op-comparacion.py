# Mayor
resultado = 5 > 4
print("5>4 = ",resultado)


# Menor
resultado = 5 < 4
print("5<4 = ",resultado)


# Mayor o igual
resultado = 5 >= 4
print("5>=4 = ",resultado)


# Menor o igual
resultado = 5 <= 4
print("5<=4 = ",resultado)


# Igual
resultado = 5 == 4
print("5==4 = ",resultado)


# Diferente
resultado = 5 != 4
print("5!=4 = ",resultado)