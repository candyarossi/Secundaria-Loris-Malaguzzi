-- Ejemplos de operadores de comparaciÃ³n

-- Mayor
local resultado = 5 > 4
print("5>4 = ",resultado)


-- Menor
local resultado = 5 < 4
print("5<4 = ",resultado)


-- Mayor o igual
local resultado = 5 >= 4
print("5>=4 = ",resultado)


-- Menor o igual
local resultado = 5 <= 4
print("5<=4 = ",resultado)


-- Igual
local resultado = 5 == 4
print("5==4 = ",resultado)


-- Diferente
local resultado = 5 ~= 4
print("5!=4 = ",resultado)