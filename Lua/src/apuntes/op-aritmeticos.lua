-- Ejemplos de operaciones aritméticas

-- Suma
local resultado = 5 + 4
print("5+4 = ",resultado)


-- Resta
local resultado = 5 - 4
print("5-4 = ",resultado)


-- Multiplicación
local resultado = 5 * 4
print("5*4 = ",resultado)


-- División
local resultado = 5 / 4
print("5/4 = ",resultado)


-- Módulo (resto de la división entera)
local resultado = 5 % 4
print("5%4 = ",resultado)


-- Exponenciación
local resultado = 5 ^ 4
print("5^4 = ",resultado)