import java.util.*;

class Main
{ 
    public static void main(String args[])
  { 
    // Mayor
    boolean resultado = 5 > 4;
    System.out.println("5>4 = " + resultado);


    // Menor
    boolean resultado = 5 < 4;
    System.out.println("5<4 = " + resultado);


    // Mayor o igual
    boolean resultado = 5 >= 4;
    System.out.println("5>=4 = " + resultado);


    // Menor o igual
    boolean resultado = 5 <= 4;
    System.out.println("5<=4 = " + resultado);


    // Igual
    boolean resultado = 5 == 4;
    System.out.println("5==4 = " + resultado);


    // Diferente
    boolean resultado = 5 != 4;
    System.out.println("5!=4 = " + resultado);

  }
}