import java.util.*;

class Main
{ 
    public static void main(String args[])
  { 
    // Suma 
    int resultado = 5 + 4;
    System.out.println("5+4 = " + resultado);


    // Resta 
    int resultado = 5 - 4;
    System.out.println("5-4 = " + resultado);


    // Multiplicación 
    int resultado = 5 * 4;
    System.out.println("5*4 = " + resultado);


    // División 
    int resultado = 5 / 4;
    System.out.println("5/4 = " + resultado);


    // Módulo (resto de una división entera) 
    int resultado = 5 % 4;
    System.out.println("5%4 = " + resultado);


    // Exponenciación 
    int resultado = 5 ^ 4;
    System.out.println("5^4 = " + resultado);

  }
}
